#include <iostream> // 引入输入输出流库，用于控制台输入输出

using namespace std; // 使用标准命名空间，避免每次调用标准库函数时都需要加std::前缀

int main() { // 主程序的入口函数
    int n; // 声明一个整型变量n，用来存储手上的空汽水瓶数量
    
    // 使用while循环读取输入，cin >> n 返回输入流对象，若输入结束或失败则为假
    while (cin >> n) { // 开始循环读取每一个测试用例的n值
        if (n == 0) { // 根据题目描述，当输入的n为0时表示输入结束
            break; // 跳出while循环，结束程序
        }
        
        // 核心逻辑：每2个空瓶实际上可以喝到1瓶汽水
        // 解释：如果有2个空瓶，向老板借1个凑成3个，换1瓶汽水，喝完剩1个空瓶还给老板，净赚1瓶汽水
        // 所以最多能喝到的汽水数量就是 n 除以 2 的整数部分
        cout << n / 2 << endl; // 输出计算结果 n/2，并输出换行符
    }
    
    return 0; // 主函数返回0，表示程序正常执行结束
}

