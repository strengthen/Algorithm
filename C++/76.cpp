#include <iostream> // 引入输入输出流库，用于控制台的输入和输出
using namespace std; // 使用标准命名空间，方便直接使用cout和cin等

int main() { // 主函数，程序的入口点
    int n; // 定义一个整型变量n，用于存储输入的整数
    
    // 尝试从标准输入读取一个整数n
    // 虽然题目通常只有一个输入，但使用while(cin >> n)可以处理多组输入的情况，更加通用
    while (cin >> n) { 
        // 根据尼科彻斯定理，n的立方可以表示为n个连续奇数的和
        // 设这n个连续奇数的首项为a，公差为2
        // 则这n个数的和 S = n*a + n*(n-1)/2 * 2 = n*a + n*(n-1)
        // 题目要求 S = n^3
        // 即 n*a + n^2 - n = n^3
        // 两边同时除以n（n>=1），得 a + n - 1 = n^2
        // 解得首项 a = n^2 - n + 1
        int start = n * n - n + 1; // 计算连续奇数数列的首项
        
        cout << start; // 先输出数列的第一个奇数
        
        // 循环输出剩下的 n-1 个奇数
        // 从1开始计数，直到i < n，共执行n-1次
        for (int i = 1; i < n; ++i) { 
            start += 2; // 计算下一个连续奇数，即当前奇数加2
            cout << "+" << start; // 按照格式要求，输出加号和当前的奇数
        }
        
        cout << endl; // 输出换行符，表示当前行的输出结束
    }
    
    return 0; // 程序执行成功，返回0
}
