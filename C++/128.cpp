#include <iostream> // 引入输入输出流库
#include <vector>   // 引入向量库（虽然本题没用到，但作为常用头文件引入）

using namespace std; // 使用标准命名空间

int main() { // 主函数入口
    long long n; // 定义长整型变量n，用于存储排列长度
    if (!(cin >> n)) return 0; // 从标准输入读取n的值，如果读取失败则退出

    long long mod = 1e9 + 7; // 定义模数 10^9 + 7

    // 计算1到n中奇数的个数。例如n=5，奇数为1,3,5，共3个；(5+1)/2 = 3
    long long cnt_odd = (n + 1) / 2; 
    // 计算1到n中偶数的个数。例如n=5，偶数为2,4，共2个；5/2 = 2
    long long cnt_even = n / 2; 

    long long ans = 1; // 初始化答案为1

    // 计算奇数个数的阶乘，即奇数位置的全排列数量
    // 比如有3个奇数，就有3!种排法
    for (long long i = 1; i <= cnt_odd; i++) { // 遍历1到cnt_odd
        ans = (ans * i) % mod; // 累乘并对mod取模，防止溢出
    }

    // 计算偶数个数的阶乘，即偶数位置的全排列数量
    // 比如有2个偶数，就有2!种排法
    for (long long i = 1; i <= cnt_even; i++) { // 遍历1到cnt_even
        ans = (ans * i) % mod; // 累乘并对mod取模，防止溢出
    }

    // 如果n是偶数，奇数和偶数数量相等
    // 满足相邻和为奇数（即一奇一偶）的排列可以是"奇偶奇偶..."，也可以是"偶奇偶奇..."
    // 所以有两种情况，需要乘以2
    if (n % 2 == 0) { // 判断n是否为偶数
        ans = (ans * 2) % mod; // 结果乘以2并对mod取模
    }
    // 如果n是奇数，奇数比偶数多1个
    // 只能是"奇偶奇偶..."这种形式（奇数开头，奇数结尾），所以只有这一种模式
    // 不需要额外乘以2

    cout << ans << endl; // 输出最终计算的结果

    return 0; // 程序正常结束返回0
}
