#include <iostream> // 引入输入输出流库，用于处理输入和输出

using namespace std; // 使用标准命名空间，方便直接使用cin, cout等

int main() { // 主函数入口，程序从这里开始执行
    int l, r; // 定义两个整型变量l和r，用来存储输入的区间左右边界
    
    // 从标准输入读取l和r的值
    while (cin >> l >> r) { // 循环读取输入，直到输入结束。虽然题目只暗示一组输入，但处理多组输入更通用
        int count = 0; // 定义计数器变量count，初始化为0，用于统计2的倍数的个数
        
        for (int i = l; i <= r; ++i) { // 使用for循环从l遍历到r，i代表当前正在判断的数字
            if (i % 2 == 0) { // 判断当前数字i是否是2的倍数（即对2取余为0）
                count++; // 如果是2的倍数，将计数器count加1
            } // if语句块结束
        } // for循环结束
        
        cout << count << endl; // 将统计得到的个数输出到控制台，并换行
    } // while循环结束

    return 0; // 程序正常执行完毕，返回0给操作系统
} // main函数结束
