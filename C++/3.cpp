#include <iostream> // 引入输入输出流头文件，用于控制台输入输出

using namespace std; // 使用标准命名空间，方便直接使用cin和cout

int main() { // 主函数入口
    int n; // 定义整型变量n，用来存储随机整数的个数
    cin >> n; // 从标准输入读取整数个数n

    // 题目中数字范围是1到500，定义一个略大于500的数组即可，这里用1001
    // 数组索引代表具体的数字，数组的值代表该数字是否出现过
    // 初始化为0，表示所有数字初始状态都未出现
    int bucket[1001] = {0}; 

    for (int i = 0; i < n; i++) { // 循环n次，读取每一个随机整数
        int temp; // 定义临时变量temp，用于存储当前输入的数字
        cin >> temp; // 读取一个整数存入temp
        bucket[temp] = 1; // 将数组中下标为temp的元素标记为1。如果数字重复出现，这里只是重复赋值为1，从而实现去重
    }

    // 题目要求从小到大排序输出
    // 我们只需要从最小可能的数(1)遍历到最大可能的数(500)
    for (int i = 1; i <= 500; i++) { 
        if (bucket[i] == 1) { // 如果桶中该位置的值为1，说明输入中包含这个数字
            cout << i << endl; // 输出该数字（即数组下标），并换行
        }
    }

    return 0; // 程序正常结束，返回0
}

