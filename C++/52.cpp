#include <iostream> // 引入输入输出流库，用于标准输入输出操作
#include <string>   // 引入字符串库，用于处理字符串类
#include <vector>   // 引入向量容器库，用于创建动态数组（DP表）
#include <algorithm> // 引入算法库，用于使用min等函数

using namespace std; // 使用标准命名空间，避免每次调用标准库需加std::前缀

int main() { // 主函数入口
    string s, t; // 定义两个字符串变量s和t，用于存储输入的两个字符串
    while (cin >> s >> t) { // 循环读取输入，直到没有更多输入（适配多组测试用例或单组）
        int n = s.length(); // 获取字符串s的长度，赋值给n
        int m = t.length(); // 获取字符串t的长度，赋值给m

        // 创建一个二维动态数组dp，大小为(n+1) x (m+1)
        // dp[i][j]表示字符串s的前i个字符和字符串t的前j个字符的编辑距离
        vector<vector<int>> dp(n + 1, vector<int>(m + 1)); 

        // 初始化边界条件：当字符串t为空时，s变为t需要删除所有字符
        for (int i = 0; i <= n; ++i) { // 遍历s的每一个长度位置
            dp[i][0] = i; // s的前i个字符变为空串需要i步删除操作
        }

        // 初始化边界条件：当字符串s为空时，s变为t需要插入所有字符
        for (int j = 0; j <= m; ++j) { // 遍历t的每一个长度位置
            dp[0][j] = j; // 空串变为t的前j个字符需要j步插入操作
        }

        // 动态规划填表过程
        for (int i = 1; i <= n; ++i) { // 遍历字符串s的第1到第n个字符
            for (int j = 1; j <= m; ++j) { // 遍历字符串t的第1到第m个字符
                if (s[i - 1] == t[j - 1]) { // 如果s的第i个字符和t的第j个字符相同（注意下标从0开始，所以是i-1和j-1）
                    dp[i][j] = dp[i - 1][j - 1]; // 不需要任何操作，编辑距离等于去掉这两个字符后的距离
                } else { // 如果字符不同，则需要进行编辑操作（增、删、改），取最小值
                    // dp[i-1][j] + 1 表示删除s的第i个字符（即s的前i-1个字符已匹配t的前j个字符，多余一个s[i-1]）
                    // dp[i][j-1] + 1 表示在s末尾插入t的第j个字符（即s的前i个字符已匹配t的前j-1个字符，缺一个t[j-1]）
                    // dp[i-1][j-1] + 1 表示将s的第i个字符替换为t的第j个字符
                    dp[i][j] = min(dp[i - 1][j] + 1, min(dp[i][j - 1] + 1, dp[i - 1][j - 1] + 1)); // 取这三种操作中的最小值
                }
            }
        }

        cout << dp[n][m] << endl; // 输出将s转换为t所需的最小编辑距离
    }
    return 0; // 程序正常结束
}
