#include <iostream> // 引入输入输出流库，用于控制台输入输出
#include <string>   // 引入字符串库，用于处理字符串对象
#include <cctype>   // 引入字符处理库，包含 toupper, tolower 等函数

using namespace std; // 使用标准命名空间，避免每次调用标准库时加 std:: 前缀

// 加密函数：对字符串进行加密操作
void Encrypt(string &s) { // 定义加密函数，参数为字符串的引用，直接修改原字符串
    for (int i = 0; i < s.length(); ++i) { // 遍历字符串中的每一个字符
        if (s[i] >= 'a' && s[i] <= 'z') { // 判断字符是否为小写字母
            if (s[i] == 'z') { // 如果是小写字母 'z'
                s[i] = 'A'; // 特殊处理：'z' 转换为大写 'A'
            } else { // 其他小写字母
                s[i] = toupper(s[i] + 1); // 字符 ASCII 码加 1 后转为大写（如 'a'->'b'->'B'）
            }
        } else if (s[i] >= 'A' && s[i] <= 'Z') { // 判断字符是否为大写字母
            if (s[i] == 'Z') { // 如果是大写字母 'Z'
                s[i] = 'a'; // 特殊处理：'Z' 转换为小写 'a'
            } else { // 其他大写字母
                s[i] = tolower(s[i] + 1); // 字符 ASCII 码加 1 后转为小写（如 'A'->'B'->'b'）
            }
        } else if (s[i] >= '0' && s[i] <= '9') { // 判断字符是否为数字
            if (s[i] == '9') { // 如果是数字 '9'
                s[i] = '0'; // 特殊处理：'9' 转换为 '0'
            } else { // 其他数字
                s[i] = s[i] + 1; // 字符 ASCII 码加 1（如 '0'->'1'）
            }
        }
    }
}

// 解密函数：对字符串进行解密操作
void Decrypt(string &s) { // 定义解密函数，参数为字符串的引用，直接修改原字符串
    for (int i = 0; i < s.length(); ++i) { // 遍历字符串中的每一个字符
        if (s[i] >= 'a' && s[i] <= 'z') { // 判断字符是否为小写字母
            if (s[i] == 'a') { // 如果是小写字母 'a'
                s[i] = 'Z'; // 特殊处理：'a' 转换为大写 'Z'
            } else { // 其他小写字母
                s[i] = toupper(s[i] - 1); // 字符 ASCII 码减 1 后转为大写（如 'b'->'a'->'A'）
            }
        } else if (s[i] >= 'A' && s[i] <= 'Z') { // 判断字符是否为大写字母
            if (s[i] == 'A') { // 如果是大写字母 'A'
                s[i] = 'z'; // 特殊处理：'A' 转换为小写 'z'
            } else { // 其他大写字母
                s[i] = tolower(s[i] - 1); // 字符 ASCII 码减 1 后转为小写（如 'B'->'A'->'a'）
            }
        } else if (s[i] >= '0' && s[i] <= '9') { // 判断字符是否为数字
            if (s[i] == '0') { // 如果是数字 '0'
                s[i] = '9'; // 特殊处理：'0' 转换为 '9'
            } else { // 其他数字
                s[i] = s[i] - 1; // 字符 ASCII 码减 1（如 '1'->'0'）
            }
        }
    }
}

// 主函数：程序的入口点
int main() { // 定义主函数
    string s1, s2; // 声明两个字符串变量，分别存储要加密和解密的字符串
    while (cin >> s1 >> s2) { // 循环读取输入，每次读取两个字符串，直到输入结束
        Encrypt(s1); // 调用加密函数处理第一个字符串
        cout << s1 << endl; // 输出加密后的结果并换行
        Decrypt(s2); // 调用解密函数处理第二个字符串
        cout << s2 << endl; // 输出解密后的结果并换行
    }
    return 0; // 程序正常运行结束，返回 0
}

