#include <iostream> // 引入输入输出流库，用于控制台输入输出
using namespace std; // 使用标准命名空间，避免每次调用标准库函数都需要加 std:: 前缀

int main() { // 主函数入口
    long n; // 定义一个长整型变量 n，用于存储输入的整数。虽然题目范围在 int 范围内，但在某些平台上 long 可能更安全
    cin >> n; // 从标准输入读取待分解的整数 n

    // 从 2 开始遍历寻找质因子，循环条件是 i 的平方小于等于 n
    // 只需要遍历到 sqrt(n)，因为如果 n 有大于 sqrt(n) 的因子，必然有一个对应的因子小于 sqrt(n)
    for (long i = 2; i * i <= n; ++i) { // 循环变量 i 从 2 开始递增，直到 i * i > n
        while (n % i == 0) { // 当 n 能够被 i 整除时，说明 i 是 n 的一个质因子
            cout << i << " "; // 输出当前的质因子 i，后面跟一个空格
            n /= i; // 将 n 除以 i，更新 n 的值，去除这个质因子，以便继续寻找下一个
        }
        // 如果 n 不能被 i 整除，循环继续，i 自增尝试下一个数
    }

    // 循环结束后，如果 n 仍然大于 1，说明剩下的 n 无法被 sqrt(n) 以前的任何数整除
    // 这意味着剩下的 n 本身就是一个质数（且大于循环结束时的 i）
    if (n > 1) { // 判断此时的 n 是否大于 1
        cout << n << " "; // 如果是，则输出这个剩下的质数 n，后面跟一个空格
    }

    cout << endl; // 输出一个换行符，虽然题目没强制要求，但通常是输出规范
    return 0; // 程序执行成功，返回 0
}

