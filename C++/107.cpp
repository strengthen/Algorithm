#include <iostream> // 引入输入输出流库，用于控制台输入输出

using namespace std; // 使用标准命名空间std

int main() { // 主函数入口
    int n, k; // 定义两个整型变量n和k，分别表示目标和与需要构造的对数
    // 读取输入的n和k。虽然题目描述只提到了第一行，但使用while循环可以处理可能的多个测试用例，且兼容单个用例
    while (cin >> n >> k) { 
        // 题目要求x和y为正整数（即x>=1, y>=1）且x + y = n
        // 由 x + y = n 可得 x = n - y
        // 因为 y >= 1，所以 x <= n - 1
        // 又因为 x >= 1，所以 x 的取值范围是 1 到 n - 1
        // 题目定义两对(x, y)不同当且仅当x不同
        // 所以最多可以构造出的不同对数为 n - 1 对
        
        if (k <= n - 1) { // 判断需要的对数k是否小于等于最大可能的对数n-1
            cout << "YES" << endl; // 如果满足条件，输出YES
        } else { // 如果需要的对数k大于最大可能的对数
            cout << "NO" << endl; // 如果不满足条件，输出NO
        }
    }
    return 0; // 返回0，表示程序正常执行完毕
}
