#include <iostream> // 引入C++标准输入输出流库
#include <cstdio>   // 引入C标准输入输出库，用于使用scanf函数

using namespace std; // 使用标准命名空间std

int main() { // 主程序的入口函数
    long long a, b, c; // 定义三个长整型变量a、b、c，用于存储输入的数值，因为题目中数值范围可达10^12

    // 使用scanf函数按照特定格式读取输入
    // 格式字符串 "%lld,%lld,...,%lld" 对应题目描述的 "a,b,...,c" 格式
    // %lld 表示读取长整型整数，其他字符（逗号和省略号）将作为分隔符被匹配和忽略
    if (scanf("%lld,%lld,...,%lld", &a, &b, &c) == 3) { // 尝试读取三个长整型数值，如果成功读取3个则进入if块
        
        // 题目要求计算省略了多少个数字
        // 序列是连续的整数，已知 a, b, ..., c
        // 省略的部分位于 b 和 c 之间
        // 计算公式为：c - b - 1
        // 例如：2,3,...,7，省略了4,5,6，数量为 7 - 3 - 1 = 3
        long long count = c - b - 1; // 计算被省略的数字数量

        cout << count << endl; // 将计算结果输出到标准输出，并换行
    }

    return 0; // 程序执行成功，返回0
}
