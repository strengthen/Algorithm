#include <iostream> // 引入输入输出流库，用于控制台输入输出

using namespace std; // 使用标准命名空间，避免每次调用标准库函数时都需要加 std:: 前缀

int main() { // 主函数入口，程序从这里开始执行
    double x; // 声明一个双精度浮点型变量 x，用于存储输入的实数
    
    cin >> x; // 从标准输入读取一个实数，并将其赋值给变量 x
    
    // 核心算法：四舍五入
    // 将输入的浮点数 x 加上 0.5，然后强制转换为整型 (int)。
    // 原理：
    // 如果小数部分 >= 0.5，加上 0.5 后整数部分会加 1（进位），强转截断小数后即为向上取整。
    // 如果小数部分 < 0.5，加上 0.5 后整数部分不变，强转截断小数后即为向下取整。
    int result = (int)(x + 0.5); // 计算四舍五入后的整数值
    
    cout << result << endl; // 将计算得到的整数结果输出到标准输出，并换行
    
    return 0; // 程序执行成功，返回整数 0 给操作系统
}




