#include <iostream> // 引入标准输入输出库，用于控制台输入输出
using namespace std; // 使用标准命名空间，避免每次调用标准库时都需要加 std:: 前缀

int main() { // 主函数入口，程序从这里开始执行
    long long l, r, x; // 声明三个长整型变量：l为区间左端点，r为区间右端点，x为给定的正整数
    
    // 尝试从标准输入读取三个整数 l, r, x
    if (cin >> l >> r >> x) { // 如果输入读取成功，则继续执行逻辑
        // 我们的目标是找到区间 [l, r] 中 x 的倍数
        // 首先计算大于等于 l 的最小的 x 的倍数
        // 使用整数除法公式 (l + x - 1) / x 相当于 ceil(l / x) 向上取整
        // 再乘以 x 即可得到大于等于 l 的最小 x 的倍数
        long long y = (l + x - 1) / x * x; // 计算候选答案 y

        // 判断这个候选答案 y 是否在区间 [l, r] 的范围内
        // 因为 y 是通过向上取整得到的，所以必然 y >= l
        // 我们只需要检查 y 是否小于等于 r
        if (y <= r) { // 如果 y 小于等于 r，说明 y 在区间 [l, r] 内
            cout << y << endl; // 输出满足条件的 y
        } else { // 如果 y 大于 r，说明区间 [l, r] 内不存在 x 的倍数
            cout << -1 << endl; // 输出 -1 表示无解
        }
    }
    
    return 0; // 程序执行完毕，返回 0 表示正常退出
}
