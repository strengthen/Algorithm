#include <iostream> // 引入输入输出流标准库，用于控制台输入输出
#include <string> // 引入字符串标准库，用于处理字符串数据
#include <vector> // 引入向量容器标准库，用于存储字符频率
#include <algorithm> // 引入算法标准库，用于调用排序函数

using namespace std; // 使用标准命名空间std，避免每次调用标准库函数时加std::前缀

int main() { // 程序的主函数入口
    int n; // 定义一个整型变量n，用于存储测试数据的组数
    while (cin >> n) { // 从标准输入读取测试数据的组数，若读取成功则进入循环
        while (n--) { // 循环n次，每次处理一组测试数据，n在每次循环后自减
            string s; // 定义一个字符串变量s，用于存储输入的字符串
            cin >> s; // 从标准输入读取一行字符串到变量s中
            vector<int> vec(26, 0); // 定义一个包含26个整数的向量，初始化为0，用于统计26个小写字母的出现次数
            for (int i = 0; i < s.length(); i++) { // 遍历字符串s中的每一个字符
                vec[s[i] - 'a']++; // 计算字符与'a'的偏移量作为索引，并将对应位置的计数加1
            } // 结束字符统计的循环
            
            sort(vec.begin(), vec.end(), greater<int>()); // 对统计数组进行降序排序，让出现次数最多的字母排在最前面
            
            int res = 0; // 定义一个整型变量res，用于存储最终计算出的漂亮度总和，初始化为0
            for (int i = 0; i < 26; i++) { // 遍历排序后的统计数组，最多遍历26个字母
                if (vec[i] == 0) { // 检查当前字母的出现次数是否为0
                    break; // 如果出现次数为0，说明剩下的字母都没出现过，直接跳出循环
                } // 结束if条件判断
                res += vec[i] * (26 - i); // 累加漂亮度：当前字母出现次数 * (26 - 当前排名索引)，排名越前权值越大
            } // 结束漂亮度计算的循环
            
            cout << res << endl; // 将计算得到的最大漂亮度输出到控制台，并换行
        } // 结束单组测试数据的处理循环
    } // 结束所有测试数据的处理循环
    return 0; // 主函数返回0，表示程序正常执行结束
} // 结束主函数定义

