#include <iostream> // 引入输入输出流库，用于控制台输入输出
using namespace std; // 使用标准命名空间，方便直接使用cout、cin等

int main() { // 主函数，程序的执行入口
    int n; // 定义一个整型变量n，用于接收可能的输入
    // 虽然题目描述说“不需要读取输入”，但示例给出了输入“1”，为了兼容性读取它
    // 在某些判题系统中，这个输入可能代表数据集的编号或者仅仅是一个占位符
    if (cin >> n) { 
        // 题目要求：百钱买百鸡。
        // 设公鸡为x只，母鸡为y只，小鸡为z只。
        // 满足两个条件：
        // 1. x + y + z = 100 (总数量为100)
        // 2. 5*x + 3*y + z/3 = 100 (总价格为100)
        
        int x, y, z; // 声明三个变量分别存储公鸡、母鸡、小鸡的数量
        
        // 公鸡一只5元，100元最多买20只，所以x的范围是0到20
        for (x = 0; x <= 20; x++) { // 第一层循环遍历公鸡的数量
            // 母鸡一只3元，100元最多买33只，所以y的范围是0到33
            for (y = 0; y <= 33; y++) { // 第二层循环遍历母鸡的数量
                z = 100 - x - y; // 小鸡的数量由总数减去公鸡和母鸡的数量确定，这样自然满足条件1
                
                // 判断是否满足条件2：
                // z % 3 == 0: 小鸡数量必须是3的倍数，因为小鸡是3只1元，不能买非整数钱的小鸡
                // 5*x + 3*y + z/3 == 100: 总花费必须等于100元
                if (z % 3 == 0 && (5 * x + 3 * y + z / 3 == 100)) {
                    // 如果满足所有条件，按格式输出公鸡、母鸡、小鸡的数量，中间用空格分隔
                    cout << x << " " << y << " " << z << endl;
                }
            }
        }
    }
    
    return 0; // 程序运行成功，返回0
}
