#include <iostream> // 引入输入输出流库
#include <algorithm> // 引入算法库，用于使用min函数
#include <cmath> // 引入数学库

using namespace std; // 使用标准命名空间，避免每次调用标准库函数时都需要加std::前缀

int main() { // 主程序的入口函数
    long long a, b, x; // 定义三个长整型变量，a表示买1只的价格，b表示买3只的价格，x表示需要购买的数量
    cin >> a >> b >> x; // 从标准输入读取三个整数a, b, x

    // 判断哪种购买方式更划算：如果买3只单只的总价小于等于买一套（3只）的价格
    if (a * 3 <= b) { 
        // 此时单买更便宜或价格一样，直接全部按单只购买
        cout << a * x << endl; // 输出总花费：单价乘以数量
    } else { 
        // 否则，说明买套装（3只一组）的平均价格更低，应优先购买套装
        long long ans = 0; // 定义变量ans用于存储最终的最小花费，初始化为0
        
        // 计算可以购买多少个完整的套装
        // x / 3 得到需要购买的套装数量
        // (x / 3) * b 计算购买这些套装的总费用
        ans += (x / 3) * b; 
        
        // 计算购买完整套装后，还剩余多少只竹鼠需要购买
        // x % 3 得到剩余的数量（可能是0, 1, 或 2）
        long long rem = x % 3; 
        
        // 处理剩余的部分，比较两种方案：
        // 1. rem * a: 按单只价格购买剩余的数量
        // 2. b: 直接再买一套（3只），虽然可能多买，但如果b比rem*a便宜则选b
        // 取两者的最小值加到总花费中
        ans += min(rem * a, b); 
        
        cout << ans << endl; // 输出最终计算出的最小花费
    }
    
    return 0; // 程序正常结束，返回0
}
