#include <iostream> // 引入输入输出流库，用于控制台输入输出

using namespace std; // 使用标准命名空间，避免每次调用标准库函数时都需要加 std:: 前缀

int main() {
    int n; // 定义一个整型变量 n，用于存储输入的整数
    // 题目保证 1 <= n <= 10^8，int 类型足以容纳
    if (cin >> n) { // 从标准输入读取整数 n，并判断读取是否成功
        bool seen[10] = {false}; // 定义一个长度为 10 的布尔数组，用来记录数字 0-9 是否已经出现过。初始化为 false

        // 使用 while 循环从右向左处理 n 的每一位数字
        // 每次循环取出最后一位，处理完后去掉最后一位
        while (n > 0) {
            int digit = n % 10; // 取出 n 的个位数字（即当前最右侧的数字）

            // 检查当前数字 digit 是否之前已经出现过
            if (!seen[digit]) {
                // 如果没有出现过，则说明这是从右向左第一次遇到该数字
                cout << digit; // 直接输出该数字。由于是从右往左取，直接输出即满足了题目要求的顺序
                seen[digit] = true; // 将该数字在 seen 数组中对应的位置标记为 true，表示已出现
            }

            n /= 10; // 将 n 除以 10，去掉刚才处理过的最后一位数字，准备处理下一位
        }
        
        cout << endl; // 输出一个换行符，保持输出格式整洁
    }
    return 0; // 程序正常结束，返回 0
}




