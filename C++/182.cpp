#include <iostream> // 引入输入输出流库，用于控制台输入输出
#include <vector> // 引入向量容器库，用于存储画作价值
#include <algorithm> // 引入算法库，主要用于使用sort函数

using namespace std; // 使用标准命名空间，避免每次调用标准库需加std::

int main() { // 程序的主入口函数
    int N, M; // 定义两个整型变量，N表示画作总数，M表示需要选出的画作数量
    if (!(cin >> N >> M)) return 0; // 从标准输入读取N和M的值，如果读取失败则直接退出程序

    vector<long long> A(N); // 定义一个long long类型的向量A，大小为N，用于存储每幅画作的艺术价值
    for (int i = 0; i < N; ++i) { // 开始一个循环，从0遍历到N-1，用于逐个读取画作价值
        cin >> A[i]; // 读取输入的一个整数，并将其存储到向量A的第i个位置
    } // 读取循环结束

    sort(A.begin(), A.end()); // 对向量A中的元素进行升序排序，以便后续通过滑动窗口找到最小差异组合

    long long min_L = -1; // 定义一个long long变量min_L用于存储最小不和谐度，初始化为-1表示尚未计算

    for (int i = 0; i <= N - M; ++i) { // 遍历排序后的数组，i表示当前选取的M个画作的起始索引，直到剩余元素不足M个
        // 计算当前选取的M个画作的不和谐度，根据排序后的性质，即为最大值的平方减去最小值的平方
        // 公式：L = |B_{M}^2 - B_{M-1}^2| + ... + |B_2^2 - B_1^2| = B_M^2 - B_1^2 （因为已排序）
        long long current_L = A[i + M - 1] * A[i + M - 1] - A[i] * A[i]; // 计算当前窗口末尾元素的平方减去起始元素的平方
        
        if (min_L == -1 || current_L < min_L) { // 检查是否是第一次计算，或者当前计算出的不和谐度小于已记录的最小值
            min_L = current_L; // 如果是，则更新最小不和谐度min_L为当前计算值
        } // 条件判断结束
    } // 滑动窗口遍历结束

    cout << min_L << endl; // 将最终计算得到的最小不和谐度输出到标准输出

    return 0; // 返回0，表示程序执行成功并正常退出
} // 主函数结束
